length=float(input('Enter the length of cuboid '))
breadth=float(input('Enter the width of cuboid '))
height=float(input('Enter the height of cuboid '))
surfacearea=2*((length*breadth)+(breadth*height)+(height*length))
volume=length*breadth*height
print('surface area of cuboid is',surfacearea)
print('volume of cuboid is',volume)
